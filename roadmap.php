            <p class="customText textJustify"><strong>Trust System</strong><strong>&nbsp;</strong> – <span style="font-size: small;">(Main Net Deployed 09/16/2018)</span><br>This milestone marks the introduction of the new trust key decay rate, with trust decreasing 3 times faster than it accrues. Replacing the hard 24-hour time limit in September 2018, the new decay rate gives staking wallets greater ability to retain trust and more accurately reflects the effort invested in securing the blockchain.</p>
            <p class="customText textJustify"><strong>Legacy Mode&nbsp;</strong>– <span style="font-size: small;">(Released 06/26/2019)</span><br>Full update to Nexus Core software supporting Legacy blockchain operation. This mode features fast sync times, instant loading, and a small memory and disk footprint.</p>
            <p class="customText textJustify"><strong>Contract Layers</strong>  – <span style="font-size: small;">(Code Complete)</span><br>The core of the Tritium Software Stack, these layers provide access to functions such as the recording of assets, managing accounts, decentralized exchange, and the creation of tokens.</p>
            <p class="customText textJustify"><strong>API / SDK&nbsp;</strong> – <span style="font-size: small;">(Code Complete)</span><br>Our API gives developers access to a wide set of features available through a simple HTTP interface. This provides easy access to smart contracts that can be embedded directly into web applications and existing login systems.</p>
            <p class="customText textJustify"><strong>Hybrid Mode&nbsp;</strong> – <span style="font-size: small;">(Pending)</span><br>An Enterprise flagship feature, hybrid mode is capable of forming an individual network out of the box, making it a highly useful tool for enterprises that wish to utilize our blockchain technology, while retaining high levels of privacy. This feature is currently capable of creating private networks with hybrid features available on the Tritium mainnet.</p>
            <p class="customText textJustify"><strong>Ambassador DAO&nbsp;</strong> – <span style="font-size: small;">(Tritium++)</span><br>Our main Tritium++ feature will govern project funding through a democratic vote. This technology is necessary for a stronger community, consensus-oriented budget allocation, and improvements to the overall governance of the network. The DAO (Decentralized Autonomous Organization) will prevent governance issues such as hard forks (Bitcoin vs Bitcoin Cash), and it will create a stronger ecosystem through community participation in decision-making, resulting overall in a more resilient and secure public network. This feature will be released with Tritium++ as a post-Tritium hard fork.</p>
            <p class="customText textJustify"><strong>LISP&nbsp;</strong> – <span style="font-size: small;">(Integrated 01/31/2019)</span><br>The Locator/ID Separation Protocol is an important network protocol that allows one to control their IP addressing, without relying on ISP’s for allocation.</p>
            <p class="customText textJustify"><strong>Interface / Wallet&nbsp;</strong> – <span style="font-size: small;">(Released 06/26/2019)</span><br>The Nexus interface supports full wallet function. Modular functionality allows custom themes and addon modules such as Binance trading, multi-coin storage, a block explorer, Tritium features, and anything module developers might dream up.</p>
