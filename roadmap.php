          <div class="headingCol customSpace mdColSpace">
            <h5>Amine</h5>
            <p class="customText textJustify"><strong>pBFT + Reputation Channels (L1)&nbsp;</strong>— This new architectural component will process transactions in parallel, using reputation as an additional weight to provide higher security. The transaction speed of L1 channels will vary based on the risk that a merchant wishes to assume, ranging from sub-second speeds to 5 seconds. For higher value transactions, it will be recommended that they receive additional weight from validation on the next consensus layer: L2, reducing transaction speed to 15 seconds plus.</p>
            <p class="customText textJustify"><strong>Network Data Sharding&nbsp;</strong>— Data sharding is an essential facet of our ledger design in order to achieve long-term scalability. Amine will provide the opportunity for nodes to run in “shard” mode, which will lower their disk and memory usage, even when the network is under high load.</p>
            <p class="customText textJustify"><strong>LLD Global File System</strong>  — The LLD global file system will support secondary files that record assets to be stored and retrieved through network operations, providing a seamless interface for managing assets and data.</p>
            <p class="customText textJustify"><strong>Domain Specific Languages&nbsp;</strong>— Contract-specific programming languages will be provided that will include internal safety mechanisms such as catching overflows to allow more complex contract development, while remaining less prone to error.</p>
            <p class="customText textJustify"><strong>DAO Voting Groups&nbsp;</strong>— This feature will extend the DAO functionality of Tritium++ by adding more groups to the consensus mechanism. Groups will be formed of individuals that share a common interest in order to create a diverse voting structure that reflects many interests in the determination of decisions that affect the entire network. Each group will have a single vote in the DAO.</p>
            <p class="customText textJustify"><strong>pBFT + PoS Trust Network (L2)&nbsp;</strong>—  As an extension to the existing Proof of Stake system, L2 will form the second layer of consensus above the L1. The L2 layer ensures safety and liveness, cross-shard communication, and resolves conflicts from the L1 layer. It represents the horizontal chaining of the L1 channels, and is a major step towards a truly decentralized and scalable ledger.</p>
            <p class="customText textJustify"><strong>LISP Multicast Links for (L1) and (L2)&nbsp;</strong>— Using LISP, the L1 and L2 layers will have their own Multicast links, therefore packets and transactions will route in constant time no matter how many nodes are part of the system.</p>
            <p class="customText textJustify"><strong>Application Store&nbsp;</strong>— Here applications and modules will be able to be shared and sold in a decentralized marketplace. Modules will provide developers the building blocks to create applications.</p>
          </div>
          <div class="headingCol customSpace mdColSpace">
            <h5>Obsidian</h5>
            <p class="customText textJustify"><strong>Extended Data Sharding&nbsp;</strong>— Data sharding in Obsidian will extend to critical network functions, resulting in nodes being required to store only a portion of the overall chain. Note, this is&nbsp;<strong>data</strong>&nbsp;sharding, not computational sharding, which means once data has been processed, it can be partitioned and stored between nodes. The result will be an increase of data storage as more nodes join the network.</p>
            <p class="customText textJustify"><strong>Decentralized Mining Pool (L3)&nbsp;</strong>— This component will use Proof of Work based mining shares computed from the work performed by the nodes of L2. Consensus will be determined by the largest value of shares + trust, in order to reach the final agreement on the most valid 3D block.</p>
            <p class="customText textJustify"><strong>Miner Reputation to improve BFT&nbsp;</strong>— This mechanism will offer a variable reward to miners depending on their reputation. It will also lay the foundations for the miner voting group, increase the security of Proof of Work on L3, and improve the decentralization of the DAO.</p>
            <p class="customText textJustify"><strong>Extending DAO Voting Groups&nbsp;</strong>— The final iteration of the DAO will have 6 voting groups with common interests: Individuals [L1], Stakers [L2], Miners [L3], Ambassadors, Developers, and Enterprises. Together the groups will ratify network upgrades, the allocation and deallocation of funding proposals, and general governance decisions.</p>
            <p class="customText textJustify"><strong>DAO: L1 Voting Group&nbsp;</strong>— The L1 voting group will provide voting rights to validators who are new to the network, and therefore do not have enough resources to participate in the L2 consensus. Voting weight is based on reputation.</p>
            <p class="customText textJustify"><strong>DAO: L2 Voting Group&nbsp;</strong><em class="markup--em markup--li-em">—&nbsp;</em>The L2 voting group will extend Tritium’s “Ambassador DAO” and provide voting rights to validators who have reached a reputation threshold. Voting weight is based on reputation multiplied by stake.</p>
            <p class="customText textJustify"><strong>DAO: L3 Voting Group&nbsp;</strong>— The L3 voting group will provide voting rights to miners. Voting weight is based on mining power (average weight of mined shares over time) multiplied by reputation.</p>
            <p class="customText textJustify"><strong>LISP Multicast Links for L3 — &nbsp;</strong>Shares on L3 will use LISP Multicast links allowing the efficient broadcasting of mined shares, and the acceptance of L2 hashes for computation by L3 nodes.</p>
